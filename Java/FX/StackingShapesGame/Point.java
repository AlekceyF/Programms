/**
 * The class <b>Point</b> keeping the position of the point. 
 * They have a x, y coordinate, type and 
 * .
 * 
 * @author Aleksey Filippov
 */
class Point {
    public int x, y; // Coordinates of the point
    public int type; // Type of the point

    public Point(int x, int y, int type) {
        this.x = x;
        this.y = y;
        this.type = type;
    }
}
